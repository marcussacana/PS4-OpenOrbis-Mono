    public class RGBColor
    {
        internal float RedF;
        internal float GreenF;
        internal float BlueF;

        public RGBColor(byte Grayscale) : this(Grayscale, Grayscale, Grayscale) { }
        public RGBColor(byte Red, byte Green, byte Blue)
        {
            RedF = Red / 255f;
            GreenF = Green / 255f;
            BlueF = Blue / 255f;
        }

        static Dictionary<string, RGBColor> NameMap = new Dictionary<string, RGBColor>();
        static RGBColor()
        {
            FieldInfo[] Colors = typeof(RGBColor).GetFields(BindingFlags.Static | BindingFlags.Public);

            foreach (var Color in Colors)
            {
                var ColorVal = Color.GetValue(null);
                var ColorName = Color.Name;

                if (!(ColorVal is RGBColor))
                    continue;

                NameMap[ColorName.ToLowerInvariant()] = (RGBColor)ColorVal;
            }
        }

        public RGBColor(string ColorCode)
        {
            int R = 0, G = 0, B = 0;
            if (NameMap.TryGetValue(ColorCode.ToLowerInvariant(), out RGBColor Target))
            {
                R = Target.R; 
                G = Target.G;
                B = Target.B;
            }
            else
            {
                if (ColorCode.StartsWith("#"))
                    ColorCode = ColorCode.Substring(1);

                switch (ColorCode.Length)
                {
                    case 3://FED
                        R = int.Parse(new string(ColorCode[0], 2), NumberStyles.HexNumber);
                        G = int.Parse(new string(ColorCode[1], 2), NumberStyles.HexNumber);
                        B = int.Parse(new string(ColorCode[2], 2), NumberStyles.HexNumber);
                        break;
                    case 4://FEDC
                        R = int.Parse(new string(ColorCode[0], 2), NumberStyles.HexNumber);
                        G = int.Parse(new string(ColorCode[1], 2), NumberStyles.HexNumber);
                        B = int.Parse(new string(ColorCode[2], 2), NumberStyles.HexNumber);
                        break;
                    case 6://FFEEDD
                        R = int.Parse(ColorCode.Substring(0, 2), NumberStyles.HexNumber);
                        G = int.Parse(ColorCode.Substring(2, 2), NumberStyles.HexNumber);
                        B = int.Parse(ColorCode.Substring(4, 2), NumberStyles.HexNumber);
                        break;
                    case 8://FFEEDDCC
                        R = int.Parse(ColorCode.Substring(0, 2), NumberStyles.HexNumber);
                        G = int.Parse(ColorCode.Substring(2, 2), NumberStyles.HexNumber);
                        B = int.Parse(ColorCode.Substring(4, 2), NumberStyles.HexNumber);
                        break;
                }
            }

            RedF = R / 255f;
            GreenF = G / 255f;
            BlueF = B / 255f;
        }

        public string AsHex()
        {
            return R.ToString("X2") + G.ToString("X2") + B.ToString("X2");
        }

        public RGBColor Grayscale()
        {
            float RGB = (RedF + GreenF + BlueF) / 3;
            return new RGBColor((byte)(255 * RGB));
        }

        public RGBColor Blend(RGBColor Secondary, byte Alpha)
        {
            float Amount = Alpha / 255f;
            byte NewR = (byte)(R * Amount + Secondary.R * (1 - Amount));
            byte NewG = (byte)(G * Amount + Secondary.G * (1 - Amount));
            byte NewB = (byte)(B * Amount + Secondary.B * (1 - Amount));
            return new RGBColor((byte)NewR, (byte)NewG, (byte)NewB);
        }

        public RGBColor Desaturate(byte Alpha)
        {
            return Blend(Black, Alpha);
        }

        public RGBColor Saturate(byte Alpha)
        {
            return Blend(White, Alpha);
        }

        public RGBColor Highlight(byte Alpha)
        {
            if (R + G + B / 3 > 255 / 2)
            {
                return Desaturate(Alpha);
            }

            return Saturate(Alpha);
        }


        public int R { get => (int)(255 * RedF); set => RedF = value / 255f; }
        public int G { get => (int)(255 * GreenF); set => GreenF = value / 255f; }
        public int B { get => (int)(255 * BlueF); set => BlueF = value / 255f; }


