namespace SixLabors.ImageSharp
{
    /// <summary>
    /// Enumerates the RGBA (red, green, blue, alpha) color components.
    /// </summary>
    internal enum RgbaComponent
    {
        /// <summary>
        /// The red component.
        /// </summary>
        R = 0,

        /// <summary>
        /// The green component.
        /// </summary>
        G = 1,

        /// <summary>
        /// The blue component.
        /// </summary>
        B = 2,

        /// <summary>
        /// The alpha component.
        /// </summary>
        A = 3
    }
}
